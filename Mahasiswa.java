public class Mahasiswa {
	private String nama;
	private String NPM;
	
	public Mahasiswa(String nama, String nPM) {
		this.nama = nama;
		NPM = nPM;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getNPM() {
		return NPM;
	}
	public void setNPM(String nPM) {
		NPM = nPM;
	}

}
